struct nw_BGR
{
	Byte b;
    Byte g;
    Byte r;
};
//---------------------------------------------------------------------------
bool operator==(const nw_BGR &c1,const nw_BGR &c2)
{
	return ((c1.b==c2.b)&&(c1.g==c2.g)&&(c1.r==c2.r));
}
//---------------------------------------------------------------------------
bool operator!=(const nw_BGR &c1,const nw_BGR &c2)
{
	return ((c1.b!=c2.b)||(c1.g!=c2.g)||(c1.r!=c2.r));
}
